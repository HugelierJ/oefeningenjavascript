let text = "";
let i = 0;
while(i<=10){
    text = text + i + " ";
    i++;
}
console.log(text);
//als je een console log eruit laat krijg je alles terug eenmalig.


//teller moet altijd achter de log komen in een While loop. anders krijg je een extra getal.

let text2= "";
let x = 0;

